#Bài 1: Tạo một set chứa các số 1, 2, 3, 4, 5 bằng hàm for và kiểm tra xem số 3 có nằm trong set đó không.​
numbers_set = set()
for i in range(1, 6):
   numbers_set.add(i)
if 3 in numbers_set:
   print('Số 3 có trong set.')
else:
   print('Số 3 không có trong set.')




